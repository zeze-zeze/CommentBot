// 送出鈕：以「錨定開頭」比對（避免子字串誤判），再排除各種媒體挑選器。
const FB_SUBMIT_RE = /^(comment|reply|post|submit|發佈|發布|送出|傳送|貼文留言)/i;
const FB_PICKER_RE =
  /(sticker|gif|avatar|emoji|photo|video|attach|with a |with an |貼圖|表情|相片|影片|大頭貼|虛擬替身|回應|聲音)/i;

function fbHasKw(s, list) {
  s = (s || '').toLowerCase();
  return list.some((k) => s.includes(k.toLowerCase()));
}
function fbIsChrome(s) {
  return (
    fbHasKw(s, ['like', 'reply', 'share', 'comment', '讚', '按讚', '回覆', '分享', '留言']) ||
    s.includes('•') ||
    /\bago\b/i.test(s) ||
    /(剛剛|小時|分鐘|天前|週前|年前|秒前)/.test(s) ||
    /^\d+\s*(min|hr|h|d|w|m|s|y)\b/i.test(s) ||
    /^\d+$/.test(s)
  );
}
// 作者：留言 article 內第一個「個人檔案連結」的文字（以 path 過濾，不看 query，
// 因為個人檔案連結的 query 常帶 comment_id）。退而求其次用 h3/h4/strong/b。
function fbAuthor(article) {
  const links = article.querySelectorAll('a[href*="facebook.com"], a[href^="/"]');
  for (const a of links) {
    const path = (a.getAttribute('href') || '').split('?')[0];
    if (/\/(comment|reply|photo|photos|hashtag|reactions?|ufi|posts|permalink|videos)\//.test(path)) continue;
    const name =
      cleanText(a.querySelector('span[dir="auto"]')?.textContent) ||
      cleanText(a.querySelector('span')?.textContent) ||
      cleanText(a.textContent);
    if (name && !fbIsChrome(name)) return name;
  }
  const h = article.querySelector('h3, h4, strong, b');
  const t = cleanText(h?.textContent);
  if (t && !fbIsChrome(t)) return t;
  return '';
}
// 內文：留言 article 內第一個合格的 dir="auto" 區塊（排除作者名、時間、UI 字樣）。
// 注意：不設長度下限（CJK 留言常很短，例如「百合男子」），改以關鍵字/時間戳過濾。
function fbText(article, author) {
  const blocks = article.querySelectorAll('div[dir="auto"]');
  for (const d of blocks) {
    const t = (d.innerText || '').trim();
    if (!t) continue;
    if (t === author) continue;
    if (/^\d+\s*(min|hr|h|d|w|m|s|y)\b/i.test(t)) continue;
    if (fbHasKw(t, ['replied', 'view replies', '已回覆', '查看回覆'])) continue;
    if (fbIsChrome(t)) continue;
    return t;
  }
  return '';
}

