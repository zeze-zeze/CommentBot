// ---------- 產生回覆（可選自動送出） ----------
// 回傳值：true 代表「已成功填入草稿」（不論送出成敗）；false 代表在填入前就中止/失敗
// （鎖忙碌、讀不到留言、無金鑰、框內已有內文、或網路/API 錯誤）。crazy 佇列據此決定是否重試。

async function generate(box, btn, status, opts = {}) {
  const { auto = false, submit = false } = opts;
  if (generating) return false;
  if (!platformEnabled()) return false;

  const comment = platform.targetComment(box);
  if (!comment || !comment.text) {
    if (!auto) setStatus(status, comment ? L('st_no_text') : L('st_no_comment'), 'err');
    return false;
  }
  // 留言篩選：不符合條件的留言，任何模式都不回覆
  if (!passesFilter(comment.text)) {
    if (!auto) setStatus(status, L('st_filtered'), '');
    return false;
  }
  if (!isConfigured()) {
    if (!auto) setStatus(status, L(cachedSettings.provider === 'custom' ? 'st_no_url' : 'st_no_key'), 'err');
    return false;
  }
  // 擴充功能被重新載入/更新後，此頁的舊內容腳本已失去連線 → 提示刷新（否則 sendMessage 會炸）
  if (!extensionAlive()) {
    if (!auto) setStatus(status, L('st_ctx_invalid'), 'err');
    return false;
  }
  // 自動流程：框內若已有真正內文，絕不覆蓋（保護使用者輸入 / 已存在的草稿）
  if (auto && splitMention(box, comment).body) return false;

  generating = true;
  const label = btn ? btn.textContent : '';
  if (btn) {
    btn.disabled = true;
    btn.textContent = L('st_generating');
  }
  setStatus(status, '', '');

  let handled = false;
  try {
    const ctx = platform.context(box);
    const resp = await chrome.runtime.sendMessage({
      type: 'GENERATE_REPLY',
      payload: {
        platform: platform.id,
        author: comment.author,
        text: comment.text.slice(0, 1500),
        title: ctx.title,
        owner: ctx.owner,
      },
    });
    if (!resp?.ok) throw new Error(resp?.error || L('st_bg_no_response'));

    await fillBox(box, resp.reply.trim(), comment);
    autoGenerated.add(commentKey(comment));
    handled = true;

    // 送出前重新確認模式：自動流程若使用者已切離 lazy/crazy，就只留草稿不送出
    const stillAuto = !auto || cachedSettings.mode === 'lazy' || cachedSettings.mode === 'crazy';
    if (submit && stillAuto && platform.submit) {
      setStatus(status, L('st_submitting'), '');
      let ok = false;
      try {
        ok = await platform.submit(box);
      } catch (_) {
        ok = false;
      }
      if (btn) btn.textContent = ok ? L('btn_sent') : btnLabel();
      setStatus(status, ok ? L('st_submitted') : L('st_submit_failed'), ok ? 'ok' : 'err');
    } else if (submit && !stillAuto) {
      if (btn) btn.textContent = L('btn_regenerate');
      setStatus(status, L('st_draft_modechanged'), 'ok');
    } else {
      if (btn) btn.textContent = L('btn_regenerate');
      setStatus(status, L('st_draft'), 'ok');
    }
  } catch (e) {
    if (btn) btn.textContent = label || btnLabel();
    setStatus(status, e.message, 'err');
  } finally {
    generating = false;
    if (btn) btn.disabled = false;
  }
  return handled;
}

// ---------- crazy 模式：自動掃描留言並逐一回覆送出 ----------

function scheduleCrazy() {
  if (!platform) return;
  if (!platformEnabled()) return; // 此平台被關閉 → 不掃描
  if (cachedSettings.mode !== 'crazy') return;
  if (crazyTimer || crazyRunning) return;
  crazyTimer = setTimeout(() => {
    crazyTimer = null;
    runCrazyQueue();
  }, 1500);
}

// 等待「本次點擊後新出現、可見、屬於回覆框、且尚無內容」的框，避免蓋到別的框或使用者輸入。
async function waitForNewBox(before) {
  for (let i = 0; i < 25; i++) {
    await sleep(150);
    for (const b of document.querySelectorAll(platform.boxSelector)) {
      if (before.has(b)) continue; // 必須是點擊後才出現的新框
      if (!isVisible(b)) continue;
      if (!platform.replyBox(b)) continue; // 套用與 focusin 相同的排除（發文框 / Messenger）
      const c = platform.targetComment(b);
      if (!c || autoGenerated.has(commentKey(c))) continue;
      if (splitMention(b, c).body) continue; // 已有內容 → 不覆蓋
      return { box: b, comment: c };
    }
  }
  return null;
}

async function runCrazyQueue() {
  if (cachedSettings.mode !== 'crazy' || crazyRunning || !platformEnabled()) return;
  if (!isConfigured() || !platform.replyButtons) return;
  crazyRunning = true;
  try {
    for (const btn of platform.replyButtons()) {
      if (cachedSettings.mode !== 'crazy' || !platformEnabled()) break; // 中途改模式/關閉平台就停
      if (btn.dataset.cbCrazy) continue; // 已成功處理／重試耗盡（永久略過）
      if (btn.dataset.cbFiltered) continue; // 本次篩選不符（settings 變更時會清除以重評）
      if (!isVisible(btn)) continue; // 尚未顯示（虛擬捲動）→ 下次掃描再處理
      // 篩選預檢：由回覆鈕直接推得所屬留言，不符合就標記「篩選略過」、連框都不開。
      // 用 cbFiltered（可回復）而非 cbCrazy（永久），這樣之後放寬/修正篩選能重新納入。
      const pre = platform.targetComment(btn);
      if (pre && !passesFilter(pre.text)) {
        btn.dataset.cbFiltered = '1';
        continue;
      }
      const tries = Number(btn.dataset.cbCrazyTries || 0);
      if (tries >= CRAZY_MAX_TRIES) {
        btn.dataset.cbCrazy = '1'; // 試過上限仍失敗 → 放棄，不再重試
        continue;
      }

      const before = new Set(document.querySelectorAll(platform.boxSelector));
      btn.click(); // 開啟該留言的回覆框
      const found = await waitForNewBox(before);
      if (cachedSettings.mode !== 'crazy') break; // 開框期間切離 crazy → 停
      if (!found) {
        btn.dataset.cbCrazyTries = String(tries + 1);
        continue;
      }
      // box 端補捉：預檢用回覆鈕推得的留言若與實際框內留言不一致（FB 邊角情況），
      // 這裡用實際留言再判一次篩選；不符合就標記篩選略過（不重試、不產生）。
      if (!passesFilter(found.comment.text)) {
        btn.dataset.cbFiltered = '1';
        continue;
      }

      const bar = ensureControlBar(found.box);
      const b = bar && bar.querySelector('button');
      const s = bar && bar.querySelector('.cb-status');
      const ok = await generate(found.box, b, s, { auto: true, submit: true });
      if (ok) btn.dataset.cbCrazy = '1'; // 已填入 → 完成（送出成敗於狀態列顯示，不重複點擊）
      else btn.dataset.cbCrazyTries = String(tries + 1); // 填入前就失敗 → 保留重試
      await sleep(CRAZY_DELAY); // 節流，避免瞬間大量送出
    }
  } finally {
    crazyRunning = false;
  }
}

// ---------- 監聽回覆框取得焦點（manual / checking / lazy） ----------

if (platform) {
  document.addEventListener(
    'focusin',
    (e) => {
      if (!platformEnabled()) return; // 此平台在設定中被關閉 → 不啟用
      const target = e.target;
      if (!(target instanceof Element)) return;
      const box = platform.replyBox(target);
      if (!box) return;

      // SPA 換頁：網址變了就清空自動產生記錄
      if (location.href !== lastHref) {
        lastHref = location.href;
        autoGenerated.clear();
      }

      const comment = platform.targetComment(box);
      if (!comment) return; // 不是「回覆某則留言」的框，略過

      const bar = ensureControlBar(box);
      if (!bar) return;
      const btn = bar.querySelector('button');
      const status = bar.querySelector('.cb-status');

      // 產生中觸發的 focusin（例如 fillBox 內部的 box.focus()）：不動 UI、不排程，避免重入
      if (generating) return;

      const body = splitMention(box, comment).body;

      // 對「空的」回覆框重置控制列外觀（把自動帶入的提及視為空）
      if (!body) {
        btn.textContent = btnLabel();
        btn.disabled = false;
        setStatus(status, '', '');
      }

      // manual：只提供手動按鈕；crazy：由掃描佇列負責，這裡不介入
      const mode = cachedSettings.mode;
      if (mode !== 'checking' && mode !== 'lazy') return;

      const cid = commentKey(comment);
      if (autoGenerated.has(cid)) return;
      if (body) return; // 使用者已自行輸入內文 → 不自動覆蓋

      // 以「每框一個排程旗標」避免重複排程；此旗標會在實際執行或放棄時清除（不像舊版永不清除）。
      if (box.dataset.cbAutoPending === cid) return;
      box.dataset.cbAutoPending = cid;

      const submit = mode === 'lazy';
      const clearPending = () => {
        if (box.dataset.cbAutoPending === cid) delete box.dataset.cbAutoPending;
      };
      // 稍等版面穩定再產生；若鎖忙碌（另一則正在產生）則稍後重試，不直接丟棄。
      const tryAuto = () => {
        if (
          autoGenerated.has(cid) ||
          splitMention(box, comment).body || // 使用者已開始打字
          cachedSettings.mode !== mode // 模式已變更 → 交給後續 focusin 處理
        ) {
          clearPending();
          return;
        }
        if (generating) {
          setTimeout(tryAuto, 300);
          return;
        }
        clearPending();
        generate(box, btn, status, { auto: true, submit });
      };
      setTimeout(tryAuto, 250);
    },
    true
  );

  // crazy 模式：監聽新留言載入（無限捲動），並在初次載入時嘗試啟動
  const mo = new MutationObserver(() => scheduleCrazy());
  mo.observe(document.documentElement, { childList: true, subtree: true });
  scheduleCrazy();
}
