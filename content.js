// YouTube CommentBot — content script
// 聚焦式（focus-driven）單則回覆：
// 當使用者點開某則留言的回覆框（該框取得焦點）時，只針對「那一則」留言，
// 透過 background 呼叫 Claude 產生一則回覆並填入回覆框，由使用者確認後手動送出。
// 一次只處理當下 focus 的一則，避免一次大量產生而消耗過多 token。

'use strict';

const BTN_LABEL = '✨ AI 產生回覆';

let cachedSettings = normalizeSettings(null);
let generating = false;

// 目前選定的供應商是否已設定 API Key
function hasApiKey() {
  const p = cachedSettings.provider;
  const k = cachedSettings.apiKeys && cachedSettings.apiKeys[p];
  return !!(k && k.trim());
}
// 本次瀏覽（單支影片）中已「自動」產生過的留言，避免重複自動觸發浪費 token。
const autoGenerated = new Set();

// ---------- 設定：讀取並跟隨變更 ----------

chrome.storage.local.get('settings').then(({ settings }) => {
  cachedSettings = normalizeSettings(settings);
});

chrome.storage.onChanged.addListener((changes, area) => {
  if (area === 'local' && changes.settings) {
    cachedSettings = normalizeSettings(changes.settings.newValue);
  }
});

// ---------- 小工具 ----------

const sleep = (ms) => new Promise((r) => setTimeout(r, ms));

function el(tag, styles, text) {
  const node = document.createElement(tag);
  if (styles) node.style.cssText = styles;
  if (text != null) node.textContent = text;
  return node;
}

function hashId(s) {
  let h = 0;
  for (let i = 0; i < s.length; i++) h = (h * 31 + s.charCodeAt(i)) | 0;
  return (h >>> 0).toString(36);
}

// ---------- 頁面資訊 ----------

function getVideoId() {
  return new URLSearchParams(location.search).get('v') || location.pathname;
}

function getVideoTitle() {
  const el = document.querySelector('ytd-watch-metadata h1 yt-formatted-string');
  return (el?.textContent || document.title.replace(/ - YouTube$/, '')).trim();
}

function getChannelInfo() {
  const a = document.querySelector(
    'ytd-video-owner-renderer #channel-name a, #owner #channel-name a'
  );
  const name = (a?.textContent || '').trim();
  const handle = ((a?.getAttribute('href') || '').match(/\/(@[^/?]+)/) || [])[1] || '';
  return { name, handle };
}

// ---------- 由回覆框找到「正在回覆的那一則留言」 ----------

// 回覆框（#contenteditable-root）位於某個 ytd-comment-thread-renderer 內。
// 取 DOM 順序上「位於回覆框之前、離它最近」的留言元素：
//  - 回覆頂層留言時 → 該頂層留言
//  - 回覆某則子留言時 → 該子留言
function findTargetComment(box) {
  const thread = box.closest('ytd-comment-thread-renderer');
  if (!thread) return null; // 例如頁面頂端「新增留言」框，不屬於任何留言，略過
  const comments = [
    ...thread.querySelectorAll('ytd-comment-view-model, ytd-comment-renderer'),
  ];
  if (!comments.length) return null;
  let target = comments[0];
  for (const c of comments) {
    // c 在 box 之前（box 在 c 之後）→ c 是候選；取最後一個符合者
    if (c.compareDocumentPosition(box) & Node.DOCUMENT_POSITION_FOLLOWING) target = c;
  }
  return target;
}

function commentText(c) {
  const node = c.querySelector('#content-text');
  return (node?.innerText || '').trim();
}

function commentAuthor(c) {
  const node = c.querySelector('#author-text');
  return (node?.textContent || '').replace(/\s+/g, ' ').trim();
}

function commentId(c) {
  return hashId(`${getVideoId()}|${commentAuthor(c)}|${commentText(c).slice(0, 120)}`);
}

// ---------- 填入回覆框 ----------

// 回覆「某則回覆」時，YouTube 會自動在框內帶入「@某人 」提及前綴（可能含 nbsp）。
// 拆出開頭的提及（可能多個）與其後真正的內文，讓我們保留提及、把回覆接在後面。
// 註：重新產生時，框內已是「@某人 上一版回覆」，此正則只抓開頭的 @token 群，
// 會把上一版內文當成 body 一併取代，因此重新產生也正確。
function mentionInfo(box) {
  const norm = (box.textContent || '').replace(/ /g, ' ');
  const m = norm.match(/^\s*((?:@\S+\s*)+)/);
  const mentions = m ? m[1].trim() : ''; // 例如 "@user" 或 "@a @b"
  const body = m ? norm.slice(m[0].length).trim() : norm.trim();
  return { mentions, body };
}

async function fillBox(box, text) {
  box.focus();
  await sleep(50);

  // 保留 YouTube 自動帶入的 @提及，把產生的回覆接在其後
  const { mentions } = mentionInfo(box);
  const finalText = mentions ? `${mentions} ${text}` : text;

  // 全選 → 刪除既有內容（含提及）→ 重新插入「提及 + 回覆」。
  // 提及在回覆框內是純文字，重插為文字不影響 YouTube 送出時的解析。
  const sel = window.getSelection();
  const range = document.createRange();
  range.selectNodeContents(box);
  sel.removeAllRanges();
  sel.addRange(range);
  document.execCommand('delete');

  // execCommand 會觸發原生 input 事件，讓 YouTube 啟用「回覆」送出按鈕
  const inserted = document.execCommand('insertText', false, finalText);
  if (!inserted || !box.textContent.trim()) {
    box.textContent = finalText;
    box.dispatchEvent(new InputEvent('input', { bubbles: true, data: finalText }));
  }
}

// ---------- 注入控制列（含產生按鈕與狀態） ----------

function getComposer(box) {
  return box.closest('ytd-commentbox') || box.closest('#reply-dialog') || box.parentElement;
}

function setStatus(status, text, kind) {
  if (!status) return;
  status.textContent = text || '';
  status.style.color = kind === 'err' ? '#f28b82' : kind === 'ok' ? '#81c995' : '#aaa';
}

function ensureControlBar(box) {
  const composer = getComposer(box);
  if (!composer) return null;

  let bar = composer.querySelector(':scope > .cb-bar');
  if (bar) return bar;

  bar = el(
    'div',
    'display:flex;align-items:center;gap:10px;margin:8px 0 4px;flex-wrap:wrap;' +
      'font:12px/1.4 Roboto,system-ui,-apple-system,"Microsoft JhengHei",sans-serif'
  );
  bar.className = 'cb-bar';

  const btn = el(
    'button',
    'display:inline-flex;align-items:center;gap:4px;padding:6px 12px;border-radius:18px;' +
      'border:none;cursor:pointer;font:600 12px Roboto,system-ui,sans-serif;' +
      'background:#3ea6ff;color:#0f0f0f',
    BTN_LABEL
  );
  btn.type = 'button';

  const status = el('span', 'font:12px Roboto,system-ui;color:#aaa');
  status.className = 'cb-status';

  btn.addEventListener('click', (e) => {
    e.preventDefault();
    e.stopPropagation();
    generate(box, btn, status, false);
  });

  bar.appendChild(btn);
  bar.appendChild(status);

  const footer = composer.querySelector('#footer');
  if (footer && footer.parentElement === composer) composer.insertBefore(bar, footer);
  else composer.appendChild(bar);

  return bar;
}

// ---------- 產生回覆 ----------

async function generate(box, btn, status, isAuto) {
  if (generating) return;

  const comment = findTargetComment(box);
  if (!comment) {
    if (!isAuto) setStatus(status, '找不到對應的留言', 'err');
    return;
  }
  const text = commentText(comment);
  if (!text) {
    if (!isAuto) setStatus(status, '讀不到留言內容', 'err');
    return;
  }
  if (!hasApiKey()) {
    // 自動模式不打擾；手動點擊才提示去設定金鑰
    if (!isAuto) setStatus(status, '尚未設定 API Key，請點工具列的擴充功能圖示設定', 'err');
    return;
  }

  generating = true;
  btn.disabled = true;
  const label = btn.textContent;
  btn.textContent = '產生中…';
  setStatus(status, '', '');

  try {
    const resp = await chrome.runtime.sendMessage({
      type: 'GENERATE_REPLY',
      payload: {
        author: commentAuthor(comment),
        text: text.slice(0, 1500),
        videoTitle: getVideoTitle(),
        channelName: getChannelInfo().name,
      },
    });
    if (!resp?.ok) throw new Error(resp?.error || '背景服務無回應');

    await fillBox(box, resp.reply.trim());
    autoGenerated.add(commentId(comment));
    btn.textContent = '🔄 重新產生';
    setStatus(status, '已填入草稿，確認後請自行按「回覆」送出', 'ok');
  } catch (e) {
    btn.textContent = label;
    setStatus(status, e.message, 'err');
  } finally {
    generating = false;
    btn.disabled = false;
  }
}

// ---------- 監聽回覆框取得焦點 ----------

document.addEventListener(
  'focusin',
  (e) => {
    const target = e.target;
    if (!(target instanceof Element)) return;
    const box = target.closest('#contenteditable-root');
    if (!box) return;

    const comment = findTargetComment(box);
    if (!comment) return; // 不是「回覆某則留言」的框（例如頁面頂端新增留言），略過

    const bar = ensureControlBar(box);
    if (!bar) return;
    const btn = bar.querySelector('button');
    const status = bar.querySelector('.cb-status');

    // 產生中觸發的 focusin（例如 fillBox 內部的 box.focus()）：不動 UI、不排程，避免重入
    if (generating) return;

    // 對「空的」回覆框重置控制列外觀（把 YouTube 自動帶入的 @提及視為空，涵蓋回覆子留言、
    // 以及 YouTube 重用同一個回覆對話框移到新留言的情況）
    if (!mentionInfo(box).body) {
      btn.textContent = BTN_LABEL;
      btn.disabled = false;
      setStatus(status, '', '');
    }

    if (!cachedSettings.autoOnFocus) return;

    const composer = getComposer(box);
    if (!composer) return;

    // 僅在框沒有「真正的內文」（只有空白或 YouTube 自動帶入的 @提及）、且該留言尚未自動
    // 產生過時觸發；以留言 id（而非布林旗標）記錄，這樣即使 YouTube 重用同一個回覆對話框
    // 移到不同留言，也能正確判斷。
    const cid = commentId(comment);
    if (autoGenerated.has(cid)) return;
    if (mentionInfo(box).body) return; // 使用者已自行輸入內文 → 不自動覆蓋
    if (composer.dataset.cbAutoComment === cid) return;

    composer.dataset.cbAutoComment = cid;
    // 稍等版面穩定再產生；此時若使用者已開始打字（框有真正內文），則放棄自動產生
    setTimeout(() => {
      if (!generating && !mentionInfo(box).body && !autoGenerated.has(cid)) {
        generate(box, btn, status, true);
      }
    }, 250);
  },
  true
);

// YouTube 是 SPA：切換影片時清空自動產生記錄，讓新影片可重新自動產生
window.addEventListener('yt-navigate-start', () => autoGenerated.clear());
